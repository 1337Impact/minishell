#include"libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	x;

	x = 0;
	if (s != 0)
	{
		while (s[x])
		{
			write(fd, &s[x], sizeof(char));
			x++;
		}
	}
}
