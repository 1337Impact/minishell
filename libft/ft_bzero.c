#include"libft.h"

void	*ft_bzero(void	*s, size_t n)
{
	size_t		x;
	char		*ss;

	ss = s;
	x = 0;
	while (x < n)
	{
		ss[x] = '\0';
		x++;
	}
	return (0);
}
